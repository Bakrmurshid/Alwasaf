const langButton = document.querySelector("[data-lang-toggle]");
const menuButton = document.querySelector("[data-menu-toggle]");
const siteNav = document.querySelector("#site-navigation");

function readValue(source, path) {
  return path.split(".").reduce((value, key) => value?.[key], source);
}

function applyLanguage(lang) {
  const content = dictionary[lang];
  const direction = lang === "ar" ? "rtl" : "ltr";
  document.documentElement.lang = lang;
  document.documentElement.dir = direction;
  document.body.dir = direction;
  document.body.dataset.lang = lang;
  document.title = content.title;
  langButton.textContent = content.toggle;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = readValue(content, element.dataset.i18n);
    if (value) element.textContent = value;
  });

  localStorage.setItem("alwasaf-language", lang);
}

function setMenuState(isOpen) {
  document.body.classList.toggle("menu-open", isOpen);
  menuButton?.setAttribute("aria-expanded", String(isOpen));
}

menuButton?.addEventListener("click", () => {
  setMenuState(!document.body.classList.contains("menu-open"));
});

siteNav?.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => setMenuState(false));
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") setMenuState(false);
});

langButton.addEventListener("click", () => {
  applyLanguage(document.body.dataset.lang === "ar" ? "en" : "ar");
  setMenuState(false);
});

applyLanguage(localStorage.getItem("alwasaf-language") || "ar");
